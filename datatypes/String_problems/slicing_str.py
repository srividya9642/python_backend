# we can return a range of characters by using the slice method.
# Specify the start index and the end index, separated by a colon, to return a part of string.abs


text = "Python"
print(text[0:3])

text = 'python'
print(text[2:5])

# Slice from start
text = 'python'
print(text[:6])

# Slice from end
text = "python"
print(text[0:])

# Negative indexing

text = 'hello world!'
print(text[-5:-2])